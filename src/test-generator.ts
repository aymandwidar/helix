/**
 * Generate vitest.config.ts for a Next.js project
 */
export function generateTestConfig(): string {
  return `import { defineConfig } from 'vitest/config';
import react from '@vitejs/plugin-react';
import path from 'path';

export default defineConfig({
  plugins: [react()],
  test: {
    environment: 'jsdom',
    globals: true,
    setupFiles: [],
    include: ['__tests__/**/*.{test,spec}.{ts,tsx}'],
    exclude: ['node_modules', '.next'],
  },
  resolve: {
    alias: {
      '@': path.resolve(__dirname, './src'),
    },
  },
});
`;
}

/**
 * Get a sample value for a field type (for test data)
 */
function getSampleValue(field: HelixField): string {
  switch (field.type) {
    case 'String':
      return `Test ${field.name}`;
    case 'Int':
      return '42';
    case 'Float':
    case 'Decimal':
      return '3.14';
    case 'Boolean':
      return 'true';
    case 'DateTime':
      return new Date().toISOString();
    case 'Json':
      return '{}';
    default:
      return `test-${field.name}`;
  }
}
